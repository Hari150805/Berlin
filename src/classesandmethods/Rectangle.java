package classesandmethods;

public class Rectangle {

    //fields
    int length;
    int  breadth;

    //length, breadth


    // constructors

    public Rectangle(int ln, int b){
        this.length=ln;
        this.breadth=b;

    }


    //methods

    public int area(){

        return length*breadth;



    }

    //method to calculate area of rectangle
}
